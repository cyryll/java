package collections.JavaTreeSets;

public class BookTs implements Comparable<BookTs> {
    int id;
    String name, author, publisher;
    int quantity;

    public BookTs(int id, String name, String author, String publisher, int quantity) {
        this.id = id;
        this.name = name;
        this.author = author;
        this.publisher = publisher;
        this.quantity = quantity;
    }

    public int compareTo(BookTs b) {
        if(id>b.id){
            return 1;
        }else if(id<b.id){
            return -1;
        }else{
            return 0;
        }
    }
}